export const NavbarButton = ({
  href,
  as: Tag = "a",
  children,
  className,
  variant = "primary",
  ...props
}: {
  href?: string;
  as?: React.ElementType;
  children: React.ReactNode;
  className?: string;
  variant?: "primary" | "secondary" | "dark" | "gradient";
} & (
  | React.ComponentPropsWithoutRef<"a">
  | React.ComponentPropsWithoutRef<"button">
)) => {
  const baseStyles =
    "px-4 py-2 rounded-md text-sm font-bold relative cursor-pointer hover:-translate-y-0.5 transition duration-200 inline-block text-center";

  const variantStyles = {
    primary: "bg-primary-red text-white hover:bg-primary-red/90 shadow-lg",
    secondary:
      "bg-transparent text-primary-blue border border-primary-blue hover:bg-primary-blue hover:text-white",
    dark: "bg-primary-dark text-white hover:bg-primary-dark/90 shadow-lg",
    gradient:
      "bg-gradient-to-b from-primary-red to-primary-blue text-white shadow-lg hover:shadow-xl",
  };

  return (
    <Tag
      href={href || undefined}
      className={cn(baseStyles, variantStyles[variant], className)}
      {...props}
    >
      {children}
    </Tag>
  );
};
